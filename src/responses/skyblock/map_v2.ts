function getValueViaDot(obj: any, key: string): any {
	return key.split('.').reduce((o, i) => {
		if (o === undefined) {
			return o;
		}
		return o[i];
	}, obj);
}

function setValueViaDot(obj: any, key: string, value: any): void {
	const keys = key.split('.');
	const lastKey = keys.pop();
	if (!lastKey) {
		return;
	}
	const lastObj = keys.reduce((o, i) => {
		if (o[i]) {
			return o[i];
		} else {
			o[i] = {};
			return o[i];
		}
	}, obj);
	lastObj[lastKey] = value;

}

function mapProfileMemberToV2(profileMember: any): void {
	// Iterate through mappings (key, value) and check if it exists in the data.
	for (const [oldKey, newKey] of Object.entries(mappings)) {
		const value = getValueViaDot(profileMember, newKey);
		if (value !== undefined) {
			setValueViaDot(profileMember, oldKey, value);
		}
	}
}

function mapProfileToV2(profile: any): void {
	if (!profile.members) {
		return;
	}
	for (const member of Object.values(profile.members)) {
		mapProfileMemberToV2(member);
	}
}

export function mapResponseToV2(path: string, response: any): void {
	if (path === 'v2/skyblock/profile') {
		mapProfileToV2(response.profile);
		return;
	}
	if (path !== 'v2/skyblock/profiles') {
		return;
	}
	if (!response.profiles) {
		return;
	}
	for (const profile of response.profiles) {
		mapProfileToV2(profile);
	}
}